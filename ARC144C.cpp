#include <bits/stdc++.h>
using namespace std;

int main() {
  int n, k;
  cin >> n >> k;
  if (k > n / 2)
    puts("-1");
  else if (n - 2 * (n / (2 * k)) * k > k) {
    for (int i = 0; i <= n / (2 * k) - 2; i++) {
      for (int j = 1; j <= k; j++)
        cout << (2 * i + 1) * k + j << " ";
      for (int j = 1; j <= k; j++)
        cout << 2 * i * k + j << " ";
    }
    for (int i = (2 * (n / (2 * k)) - 1) * k + 1; i <= (2 * (n / (2 * k))) * k;
         i++)
      cout << i << " ";
    for (int i = (2 * (n / (2 * k)) - 2) * k + 1;
         i <= (2 * (n / (2 * k)) - 2) * k + n % k; i++)
      cout << i << " ";
    for (int i = n - k + 1; i <= n; i++)
      cout << i << " ";
    for (int i = (2 * (n / (2 * k)) - 2) * k + n % k + 1;
         i <= (2 * (n / (2 * k)) - 1) * k; i++)
      cout << i << " ";
    for (int i = (2 * (n / (2 * k))) * k + 1;
         i <= (2 * (n / (2 * k))) * k + n % k; i++)
      cout << i << " ";
    cout << "\n";
  } else {
    for (int i = 0; i <= n / (2 * k) - 2; i++) {
      for (int j = 1; j <= k; j++)
        cout << (2 * i + 1) * k + j << " ";
      for (int j = 1; j <= k; j++)
        cout << 2 * i * k + j << " ";
    }
    for (int i = (2 * (n / (2 * k)) - 1) * k + 1; i <= n; i++)
      cout << i << " ";
    for (int i = (2 * (n / (2 * k)) - 2) * k + 1;
         i <= (2 * (n / (2 * k)) - 1) * k; i++)
      cout << i << " ";
    cout << "\n";
  }
  return 0;
}

